    if user_num < my_list[len(my_list) - 1]:
        my_list.append(user_num)
        break
    if user_num >= element:
        if user_num == element:
            k = my_list.count(element) + my_list.index(element)
            my_list.insert(k, user_num)
            break
        else:
            my_list.insert(k, user_num)
        break
    if user_num < element:
        k = k + 1
