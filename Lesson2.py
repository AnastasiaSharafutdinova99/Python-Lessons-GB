#    В кортеже должно быть два элемента — номер товара и словарь с параметрами,
#    то есть характеристиками товара: название, цена, количество, единица измерения.
#    Структуру нужно сформировать программно, запросив все данные у пользователя.
#    Пример готовой структуры:
#
#    [
#    (1, {“название”: “компьютер”, “цена”: 20000, “количество”: 5, “eд”: “шт.”}),
#    (2, {“название”: “принтер”, “цена”: 6000, “количество”: 2, “eд”: “шт.”}),
#    (3, {“название”: “сканер”, “цена”: 2000, “количество”: 7, “eд”: “шт.”})
#    ]
#
#    Нужно собрать аналитику о товарах.
#    Реализовать словарь, в котором каждый ключ — характеристика товара, например, название.
#    Тогда значение — список значений-характеристик, например, список названий товаров.
#    Пример:
#
#    {
#    “название”: [“компьютер”, “принтер”, “сканер”],
#    “цена”: [20000, 6000, 2000],
#    “количество”: [5, 2, 7],
#    “ед”: [“шт.”]
#    }
goods_count = int(input('Введите количество товаров: '))
tuple_count = 0
name_list = []
cost_list = []
count_list = []
units_list = []
tuple_list = []
while tuple_count < goods_count:
    name = input('Введите название: ')
    name_list.append(name)
    cost = int(input('Введите цену: '))
    cost_list.append(cost)
    count = int(input('Введите количество: '))
    count_list.append(count)
    units = input('Введите единицы измерения: ')
    units_list.append(units)
    goods = (tuple_count + 1, {'название': name, 'цена': cost, 'количество': count, 'eд': units})
    tuple_list.append(goods)
    tuple_count = tuple_count + 1
print(tuple_list)
analytics = {'название': name_list, 'цена': cost_list, 'количество': count_list, 'ед': units_list}
